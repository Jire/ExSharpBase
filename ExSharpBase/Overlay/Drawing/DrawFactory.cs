        public static void DrawCircleRange(Vector3 position, float radius, Color color, float thickness,
            bool filled = false)
        {
            if (Effect == null || Effect.IsDisposed)
                return;
            var vertexDeclaration = device.VertexDeclaration;
            Effect.Begin();
            device.SetStreamSource(0, VertexBuffer, 0, Utilities.SizeOf<Vector4>());
            device.VertexDeclaration = VertexDeclaration;
            var viewMatrix = Renderer.GetViewMatrix();
            var projectionMatrix = Renderer.GetProjectionMatrix();
            var value = position;
            Effect.BeginPass(0);
            Effect.SetValue("ProjectionMatrix", Matrix.Translation(value) * viewMatrix * projectionMatrix);
            Effect.SetValue("Color",
                new Vector4(color.R / 255f, color.G / 255f, color.B / 255f,
                    color.A / 255f));
            Effect.SetValue("Radius", radius);
            Effect.SetValue("Width", thickness);
            Effect.SetValue("Filled", filled);
            Effect.SetValue("EnableZ", false);
            Effect.SetValue("antiAlias", 1.25f);
            Effect.EndPass();
            device.DrawPrimitives(PrimitiveType.TriangleList, 0, 1);
            Effect.CommitChanges();
            Effect.End();
            device.VertexDeclaration = vertexDeclaration;
        }

